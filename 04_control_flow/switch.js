// SWITCH case use for any type of variable like number,string,boolean etc.
const month=11;
switch(month){
    case 1:
        console.log("JANUARY");
        break;
    case 2:
        console.log("FEBRUARY");
        break;
    case 3:
        console.log("MARCH");
        break;
    case 4:
        console.log("APRIL");
        break;
    case 5:
        console.log("MAY");
        break;
    case 6:
        console.log("JUNE");
        break;
    case 7:
        console.log("JULY");
        break;
    case 8:
        console.log("AUG");
        break;
    case 9:
        console.log("SEPT");
        break;
    case 10:
        console.log("OCTOBER");
        break;
    case 11:
        console.log("NOVEMBER");
        break;
    case 12:
        console.log("DECEMBER");
        break;
    default:
        console.log("NOT A MONTH");
        break;
}